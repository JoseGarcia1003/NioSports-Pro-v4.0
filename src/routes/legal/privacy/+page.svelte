  <h1>Política de Privacidad</h1>
  <p class="legal__updated">Última actualización: Marzo 2026</p>

  <h2>1. Datos que Recopilamos</h2>
  <p>Email y nombre (al registrarse), picks guardados, configuración de bankroll, y datos de uso anónimos para mejorar el servicio.</p>

  <h2>2. Cómo Usamos sus Datos</h2>
  <p>Sus datos se usan exclusivamente para: proporcionar el servicio, calcular métricas de rendimiento personalizadas, enviar notificaciones que usted active, y mejorar nuestros modelos predictivos con datos agregados y anonimizados.</p>

  <h2>3. Datos que NO Vendemos</h2>
  <p><strong>No vendemos, compartimos ni cedemos sus datos personales a terceros.</strong> Punto.</p>

  <h2>4. Almacenamiento y Seguridad</h2>
  <p>Los datos se almacenan en Supabase (PostgreSQL) con cifrado en tránsito y reposo. Los pagos son procesados por Stripe; nunca almacenamos datos de tarjetas de crédito.</p>

  <h2>5. Sus Derechos</h2>
  <p>Puede solicitar en cualquier momento: acceso a sus datos, corrección, eliminación completa, o exportación. Contacte a contacto@niosports.pro.</p>

  <h2>6. Cookies</h2>
  <p>Usamos cookies esenciales para autenticación y preferencias (tema, sesión). No usamos cookies de tracking ni publicidad.</p>
